#!/usr/bin/env node
/**
 * Generate the Loop24 Signal theme TS const from the canonical JSON palette.
 *
 * Canonical source: src/resources/extensions/loop24/theme/loop24.json
 * Generated output: packages/pi-coding-agent/src/modes/interactive/theme/loop24-theme.generated.ts
 *
 * Why a generator: pi-coding-agent's tsconfig has rootDir: ./src which
 * prevents importing JSON from outside the package boundary. Rather than
 * relax rootDir (cross-package coupling) or duplicate the palette by hand
 * (Phase 11 deferred-cleanup item #13), we emit a typed const from the JSON
 * at build time. Runs on prebuild — edit ONLY the JSON.
 *
 * Mirrors the existing scripts/sync-piconfig.mjs pattern.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const SOURCE = join(
  ROOT,
  "src",
  "resources",
  "extensions",
  "loop24",
  "theme",
  "loop24.json",
);
const TARGET = join(
  ROOT,
  "packages",
  "pi-coding-agent",
  "src",
  "modes",
  "interactive",
  "theme",
  "loop24-theme.generated.ts",
);

const HEADER = `// AUTO-GENERATED from src/resources/extensions/loop24/theme/loop24.json
// by scripts/generate-theme-const.mjs (runs on prebuild).
// Do not edit this file directly — edit the JSON and run \`npm run build\`
// (or \`npm run generate-theme-const\`).

import type { ThemeJson } from "./theme-schema.js";

`;

function stripSchemaKey(obj) {
  const { $schema, ...rest } = obj;
  return rest;
}

function unquotePropertyNames(jsonText) {
  // Turn `"key": value` into `key: value` for valid JS identifier keys only.
  // Leaves keys with hyphens / special chars quoted.
  return jsonText.replace(/^(\s*)"([A-Za-z_$][A-Za-z0-9_$]*)":/gm, "$1$2:");
}

const raw = readFileSync(SOURCE, "utf-8");
const parsed = stripSchemaKey(JSON.parse(raw));

const bodyJson = JSON.stringify(parsed, null, "\t");
const body = unquotePropertyNames(bodyJson);

const output = `${HEADER}export const loop24Theme: ThemeJson = ${body};\n`;

let existing = null;
try {
  existing = readFileSync(TARGET, "utf-8");
} catch {
  // first run — target doesn't exist yet
}

if (existing === output) {
  console.log(
    "[generate-theme-const] loop24-theme.generated.ts already up-to-date",
  );
} else {
  writeFileSync(TARGET, output, "utf-8");
  console.log(
    `[generate-theme-const] wrote ${TARGET.replace(ROOT + "/", "")}`,
  );
}

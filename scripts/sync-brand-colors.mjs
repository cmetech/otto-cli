#!/usr/bin/env node
/**
 * Sync src/brand-colors.ts → src/resources/brand-colors.ts.
 *
 * The two tsconfigs (main = rootDir:src + excludes resources; resources =
 * rootDir:src/resources) split the source tree into two sealed compile
 * contexts. Neither can import from the other. Brand colors must be readable
 * by code in both — so we keep ONE canonical file (src/brand-colors.ts) and
 * mirror it into the resources tree. This script runs in `prebuild` to keep
 * the mirror current.
 *
 * If anything else in the project needs cross-tree shared constants, follow
 * the same pattern: canonical file in src/, mirror into src/resources/ here.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const CANONICAL = join(ROOT, "src", "brand-colors.ts");
const MIRROR = join(ROOT, "src", "resources", "brand-colors.ts");

const HEADER = `// ─────────────────────────────────────────────────────────────────────────────
// AUTO-GENERATED MIRROR of src/brand-colors.ts.
// DO NOT EDIT THIS FILE DIRECTLY — edit src/brand-colors.ts and run
//   npm run build         (prebuild syncs this file)
// or to sync without a full build:
//   node scripts/sync-brand-colors.mjs
// The two-file split exists because tsconfig.resources.json has
// rootDir: src/resources and can't reach files outside that tree.
// ─────────────────────────────────────────────────────────────────────────────

`;

const canonical = readFileSync(CANONICAL, "utf-8");
writeFileSync(MIRROR, HEADER + canonical, "utf-8");
console.log(`[sync-brand-colors] ${CANONICAL.replace(ROOT + "/", "")} → ${MIRROR.replace(ROOT + "/", "")}`);
